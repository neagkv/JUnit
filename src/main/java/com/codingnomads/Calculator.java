package com.codingnomads;

public class Calculator {

    public double add (double x, double y){
        return x + y;
    }

    public double subtract(double x, double y){

        return (x - y);
    }

    public double mult(double x, double y){

        return x * y;
    }

    public double divide( double x, double y){

        return (x/y);
    }
}
